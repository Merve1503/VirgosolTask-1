public class Task5 {

    /*
    5- Java'da bir değişkenin değerini başka bir değişkene atamanın nasıl yapıldığını açıklayan bir program yazınız.
    `int a = 10;` ve `int b;` başlangıç değerlerini kullanın:
    */

    public static void main(String[] args) {

        int a = 10;
        int b;

        // Değişkenin değerinin başka bir değişkene atanması
        b = a;

        // Sonuçların ekrana yazdırılması
        System.out.println("Değişken a'nın değeri: " + a);
        System.out.println("Değişken b'nin değeri: " + b);

    }

}
